
# join
print(", ".join(["spam", "marigoldo", "yoman"]))

# replace
print("Hello ME".replace("ME", "World"))

# startswith
print("This is a car".startswith("This"))

# endswith
print("This is a car".endswith("car"))

# upper
print("This is a car".upper())

# lower
print("THIS IS A CAR".lower())

# split
print("spam eggs ham".split(", "))